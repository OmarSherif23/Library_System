    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String sq1 = "select * from user where ID_user=? and password =? ";
       
 
         try{
       
       Class.forName("com.mysql.jdbc.Driver");
       
       Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library system","root","");
       PreparedStatement stmt = conn.prepareStatement(sq1);
       

       
       stmt.setString(1,jTextField1.getText());
       stmt.setString(2,new String(jPasswordField1.getPassword()));
       
       
        ResultSet rs = stmt.executeQuery();
       

       
        if(rs.next())
        {
          
           String userType = rs.getString("type");
                    if(userType.equals("librarian")){
                         this.setVisible(false);
                         Librarian ap = new Librarian();
                         
                         
                    }else if(userType.equals("reader"))
                    {
                        String isBlocked = rs.getString("isblocked");
                  if(isBlocked.equals("not blocked"))
                  {  
                      this.setVisible(false);
                      Reader ap = new Reader();
                      
                  }
                  else
                  {
                   JOptionPane.showMessageDialog(null, "This user is blocked!");
                  }
                    }
           
        }
        else if( jTextField1.getText().isEmpty() || jPasswordField1.getText().isEmpty()) {
       
       JOptionPane.showMessageDialog(null, "Field is empty");

       } 
        
        else
        {
         JOptionPane.showMessageDialog(null, "insertion unsuccessful");
        }
       
       conn.close();
       }catch(Exception e)       
       {
           JOptionPane.showMessageDialog(null, e);
       }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

